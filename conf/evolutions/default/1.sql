  `DESTINATION_LOCATION_ID` TEXT NOT NULL,
  `TIME_PACKED` TEXT,
  `TIME_SENT` TEXT,
  `TIME_RECEIVED` TEXT,
  `TIME_UNPACKED` TEXT,
  `TIME_COMPLETED` TEXT
);

