  `STATE` TEXT NOT NULL,
  `COURIER_NAME` TEXT NOT NULL,
  `TRACKING_NUMBER` TEXT NOT NULL,
  `ORIGIN_CENTRE_ID` TEXT NOT NULL,
  `ORIGIN_LOCATION_ID` TEXT NOT NULL,
